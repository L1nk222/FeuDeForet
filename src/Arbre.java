import java.awt.*;

public class Arbre {

    private Color color;
    private Point position;

    public Arbre(Color color, Point position) {
        this.color = color;
        this.position = position;
    }

    public Point getPosition() {
        return position;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }





}
